    ;========================
    ; decisão
    ;========================

    PUSH 3
    PUSH 23
    CALL TEXT_SET_CURPOS

    PUSH RED
    PUSH OFFSET TXT_DECISION
    CALL TEXT_COLORSTR

WAIT_INPUT:

    MOV AH, 00h
    INT 16h

    CMP AH, 4Bh
    JNE @@NOT_LEFT
    JMP INPUT_LEFT

@@NOT_LEFT:

    CMP AH, 4Dh
    JNE @@NOT_RIGHT
    JMP INPUT_RIGHT

@@NOT_RIGHT:

    JMP WAIT_INPUT

INPUT_LEFT:

    MOV AL, 1
    JMP CHECK_RESP

INPUT_RIGHT:

    MOV AL, 0

CHECK_RESP:

    MOV BX, [NPC_INDEX]

    MOV SI, OFFSET RESPS
    ADD SI, BX

    MOV BL, [SI]

    CMP AL, BL
    JE @@CORRECT
    JMP GAME_OVER

@@CORRECT:

    PUSH 5
    PUSH 25
    CALL TEXT_SET_CURPOS

    PUSH GREEN
    PUSH OFFSET TXT_OK
    CALL TEXT_COLORSTR

    MOV AH, 00
    INT 16h

    INC [NPC_INDEX]

    JMP GAME_LOOP

GAME_OVER:

    PUSH 5
    PUSH 25
    CALL TEXT_SET_CURPOS

    PUSH RED
    PUSH OFFSET TXT_BAD
    CALL TEXT_COLORSTR

    MOV AH, 00
    INT 16h

    RET

WIN_GAME:

    PUSH 10
    PUSH 12
    CALL TEXT_SET_CURPOS

    PUSH GREEN
    PUSH OFFSET TXT_WIN
    CALL TEXT_COLORSTR

    MOV AH, 00
    INT 16h

    RET

;}
ENDP MENU