    CALL TEXT_COLORSTR

    PUSH 3
    PUSH 19
    CALL TEXT_SET_CURPOS
    PUSH WHITE
    PUSH OFFSET DFNPC1I
    CALL TEXT_COLORSTR

    PUSH 3
    PUSH 21
    CALL TEXT_SET_CURPOS
    PUSH WHITE
    PUSH OFFSET DFNPC1J
    CALL TEXT_COLORSTR

    JMP @@END_PRINT

    ;=================================================
    ; NPC 2
    ;=================================================

@@NPC2:

    CMP [NPC_INDEX], 1
    JE @@PRINT_NPC2
    JMP @@NPC3

@@PRINT_NPC2:

    PUSH 3
    PUSH 3
    CALL TEXT_SET_CURPOS
    PUSH WHITE
    PUSH OFFSET DFNPC2
    CALL TEXT_COLORSTR

    PUSH 3
    PUSH 5
    CALL TEXT_SET_CURPOS
    PUSH WHITE
    PUSH OFFSET DFNPC2B
    CALL TEXT_COLORSTR

